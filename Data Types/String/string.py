s = "Hello"
s2 = "i Am a proGrammer"

print(s.count('l'))  #counting occurrence of 'l'

print(s2.capitalize())  #sorting the problem of capitalization

print(s + " " + str(1))  #adding a value with string

print(s + '.' + s2)  #combining 2 strings

print(s.lower())  #all characters in small letter

print(s.upper())  #all characters in capital letter

print(s.find('l'))  #finding the index number

print(s.find('l') + 1)  #finding the index number after 'l'